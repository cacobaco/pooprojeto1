import greenfoot.*;  // (World, Actor, GreenfootImage, Greenfoot and MouseInfo)

public class PressurePlate extends TransposableObject {

    // pressure plate invisible object
    public PressurePlate(int width, int height) {
        super(width, height);
    }

    // pressure plate object with given image path
    public PressurePlate(String image) {
        super(image);
    }

    // pressure plate object with given image object
    public PressurePlate(GreenfootImage image) {
        super(image);
    }
    
}
